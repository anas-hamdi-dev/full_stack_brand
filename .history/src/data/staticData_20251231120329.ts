// Function to add a new brand (for brand owner sign-up functionality)
export function addBrand(brand: Omit<Brand, "id" | "created_at" | "updated_at" | "categories">): Brand {
  const category = staticCategories.find(c => c.id === brand.category_id);
  const newBrand: Brand = {
    ...brand,
    id: String(_staticBrands.length + 1),
    created_at: new Date().toISOString(),
    updated_at: new Date().toISOString(),
    categories: category ? { name: category.name } : null,
  };
  _staticBrands.push(newBrand);
  return newBrand;
}

// Function to update an existing brand
export function updateBrand(
  brandId: string,
  updates: Partial<Omit<Brand, "id" | "created_at" | "updated_at" | "categories">>
): Brand {
  const brandIndex = _staticBrands.findIndex(b => b.id === brandId);
  if (brandIndex === -1) {
    throw new Error(`Brand with id ${brandId} not found`);
  }

  const existingBrand = _staticBrands[brandIndex];
  const category = updates.category_id 
    ? staticCategories.find(c => c.id === updates.category_id)
    : staticCategories.find(c => c.id === existingBrand.category_id);

  const updatedBrand: Brand = {
    ...existingBrand,
    ...updates,
    updated_at: new Date().toISOString(),
    categories: category ? { name: category.name } : existingBrand.categories,
  };

  _staticBrands[brandIndex] = updatedBrand;
  return updatedBrand;
}

