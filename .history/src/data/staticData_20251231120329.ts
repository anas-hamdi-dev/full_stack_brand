// Static data for the application
export interface Brand {
  id: string;
  name: string;
  category_id: string | null;
  description: string | null;
  logo_url: string | null;
  location: string | null;
  website: string | null;
  instagram: string | null;
  facebook: string | null;
  phone: string | null;
  email: string | null;
  is_featured: boolean | null;
  created_at: string;
  updated_at: string;
  categories?: {
    name: string;
  } | null;
}

export interface Category {
  id: string;
  name: string;
  icon: string;
  description: string | null;
  brand_count: number | null;
  created_at: string;
}

export interface Product {
  id: string;
  name: string;
  description: string | null;
  brand_id: string | null;
  price: number | null;
  images: string[];
  external_url: string | null;
  created_at: string;
  brands?: {
    id: string;
    name: string;
    logo_url: string | null;
    website: string | null;
  } | null;
}

export interface BrandSubmission {
  id: string;
  brand_name: string;
  category: string;
  description?: string | null;
  contact_email: string;
  contact_phone?: string | null;
  website?: string | null;
  instagram?: string | null;
  status: string;
  created_at: string;
}

export interface ContactMessage {
  id: string;
  name: string;
  email: string;
  subject: string;
  message: string;
  status: string;
  created_at: string;
}

export interface User {
  id: string;
  email: string;
  password: string; // In production, this would be hashed
  full_name: string;
  first_name?: string;
  last_name?: string;
  phone?: string | null;
  role: "client" | "brand_owner";
  avatar_url?: string | null;
  brand_id?: string | null; // For brand owners, link to their brand
  created_at: string;
  updated_at: string;
}

